package com.company;

public class Calculos {
    //Atributos
    public int num1;
    public int num2;

    //Constructor
    public Calculos(){
    }
    public Calculos (int num1, int num2){
        this.num1 = num1;
        this.num2 = num2;
    }

    public int sumar(){
        int suma = this.num1 = this.num2;
        return suma;
    }

}
